package lang.string;

public class StringBasicMain {
    public static void main(String[] args) {
        String str1 = "Marine"; //쌍따옴표 사용
        String str2 = new String("Marine"); //객체 생성
        System.out.println("str1 = " + str1);
        System.out.println("str2 = " + str2);

    }
}
// - 결과값 -
